export function translateTendance(tendance: string, t: Record<string, string>): string {
  const map: Record<string, string> = {
    emergence: t.tendEmergence,
    disparition: t.tendDisparition,
    stable: t.tendStable,
    croissance: t.tendCroissance,
    declin: t.tendDeclin,
  };
  return map[tendance.toLowerCase()] || tendance;
}
